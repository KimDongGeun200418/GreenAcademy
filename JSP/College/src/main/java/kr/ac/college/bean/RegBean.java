package kr.ac.college.bean;

public class RegBean {
	private String regStdNo;
	private String regLecNo;
	private String regMidScore;
	private String regFinalScore;
	private String regTotalScore;
	
	private String regGrade;
	
	//추가필드
	private String regStdName;
	private String regLecName;

	
	public String getRegStdNo() {
		return regStdNo;
	}
	public void setRegStdNo(String regStdNo) {
		this.regStdNo = regStdNo;
	}

	public String getRegLecNo() {
		return regLecNo;
	}
	public void setRegLecNo(String regLecNo) {
		this.regLecNo = regLecNo;
	}

	public String getRegMidScore() {
		return regMidScore;
	}
	public void setRegMidScore(String regMidScore) {
		this.regMidScore = regMidScore;
	}

	public String getRegFinalScore() {
		return regFinalScore;
	}
	public void setRegFinalScore(String regFinalScore) {
		this.regFinalScore = regFinalScore;
	}

	public String getRegTotalScore() {
		return regTotalScore;
	}
	public void setRegTotalScore(String regTotalScore) {
		this.regTotalScore = regTotalScore;
	}

	public String getRegGrade() {
		return regGrade;
	}
	public void setRegGrade(String regGrade) {
		this.regGrade = regGrade;
	}

	
	public String getRegStdName() {
		return regStdName;
	}
	public void setRegStdName(String regStdName) {
		this.regStdName = regStdName;
	}
	
	public String getRegLecName() {
		return regLecName;
	}
	public void setRegLecName(String regLecName) {
		this.regLecName = regLecName;
	}
}
